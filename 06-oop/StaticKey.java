public class StaticKey{
    //normal variable
    int x=25;

    //static variable
    static int y=50;

    //normal method
    public void myMethod(){
        System.out.println("Understanding the static key word...");
    }

    //static method
    public static void staticMethod(){
        System.out.println("Static method");
    }
}

